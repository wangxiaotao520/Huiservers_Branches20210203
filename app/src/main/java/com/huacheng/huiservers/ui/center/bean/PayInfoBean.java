package com.huacheng.huiservers.ui.center.bean;

public class PayInfoBean {
	private  String secret_string;
	private String path;
	private String compel;
	private String mgs;

	private String version;

	public String getMgs() {
		return mgs;
	}

	public void setMgs(String mgs) {
		this.mgs = mgs;
	}

	public String getCompel() {
		return compel;
	}

	public void setCompel(String compel) {
		this.compel = compel;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getSecret_string() {
		return secret_string;
	}

	public void setSecret_string(String secret_string) {
		this.secret_string = secret_string;
	}


	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

}
